import { 哈希表, 阴阳, 引言, 构筑, 空 } from "./神言.js";

const 点到为止 = 构筑(哈希表)
const 化劲 = 引言(空)
export function 化(
  武德
) {
  const 润 = () => {
    化劲.言 = 武德
    武德?.()
  }

  润()
  return 润
}

export function 好自为之(言) {
  const 好言相劝 = 引言(言)
  const 接 = () => {
    const 年轻人 = 化劲.言
    if (年轻人) {
      let 听我说
      if (!点到为止.似乎(年轻人)) {
        点到为止.钦定(好言相劝, 听我说 = 构筑(阴阳))
      } else {
        听我说 = 点到为止.一眼顶针(好言相劝)
      }
      听我说.加封(年轻人)
    }
    return 好言相劝.言
  }
  const 发 = (力) => {
    好言相劝.言 = 力(好言相劝.言)
    const 左正蹬 = 点到为止.一眼顶针(好言相劝)
    for (const 右鞭腿 of 左正蹬 ?? []) {
      右鞭腿()
    }
  }

  return {
    接, 发
  }
}
